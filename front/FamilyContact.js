class FamilyContact {

    constructor(id, name, firstLastName, secondLastName, phone, address, friend) { 
        this.id = id;
        this.name = name;
        this.firstLastName = firstLastName;
        this.secondLastName = secondLastName;
        this.phone = phone;
        this.address = address;
        this.friend = friend;
    }
}
