class WorkContact {

    constructor(id, name, firstLastName, secondLastName, phone, address, companyName, area, businessSector) { 
        this.id = id;
        this.name = name;
        this.firstLastName = firstLastName;
        this.secondLastName = secondLastName;
        this.phone = phone;
        this.address = address;
        this.companyName = companyName;
        this.area = area;
        this.businessSector = businessSector;
    }
}
